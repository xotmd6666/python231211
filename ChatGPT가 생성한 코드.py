# 리스트 생성
my_list = [1, 2, 3, 4, 5]

# 튜플 생성
my_tuple = (1, 2, 3, 4, 5)

# 세트 생성
my_set = {1, 2, 3, 4, 5}

# 딕셔너리 생성
my_dict = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}

# 타입 출력
print("List 타입:", type(my_list))
print("Tuple 타입:", type(my_tuple))
print("Set 타입:", type(my_set))
print("Dict 타입:", type(my_dict))

# 길이 출력
print("List 길이:", len(my_list))
print("Tuple 길이:", len(my_tuple))
print("Set 길이:", len(my_set))
print("Dict 길이:", len(my_dict))

# 원소 출력
print("List 원소:", my_list)
print("Tuple 원소:", my_tuple)
print("Set 원소:", my_set)
print("Dict 원소:", my_dict)
